public class _2Logical_Operators {
    public static void main(String[] args) {
        
        int temp = 20;

        // Logical AND
        boolean isBig = temp > 10 && temp <30;
        System.out.println(isBig);
        
        // Logical OR

        boolean isSmall = temp > 10 || temp <30;
        System.out.println(isSmall);
    }
}
